void main() {
  int number = 4;
  if (number == 0) {
    print("Zero");
  } else if (number == 1) {
    print("One");
  } else if (number == 2) {
    print("Two");
  } else if (number == 3) {
    print("Three");
  } else if (number == 4) {
    print("Four");
  } else if (number == 5) {
    print("Five");
  } else {
    print("Entered number is greater than 5");
  }
}
